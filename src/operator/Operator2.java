package operator;

public class Operator2 {
    public static void main(String[] args) {
        // 문자열 더하기
        // 문자열에도 + 연산자를 사용하여 연결할 수 있다
        String result = "Hello" +" " + "JAVA";
        System.out.printf("result = %s\n", result);

        // 문자열에 숫자 더하기
        String result2 = "a + b = " + 10;
        System.out.printf("result2 = %s\n", result2);

    }
}
